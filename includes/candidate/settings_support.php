        <div class="resource-content" id="resource-legal-rights" style="display: none;">
            <h4>Your Legal Rights as a Job Seeker</h4>
            <p>Job seekers with disabilities are protected by various laws designed to ensure equal employment opportunities.</p>
            <h5>Key Protections:</h5>
            <ul>
                <li><strong>Non-Discrimination:</strong> Employers cannot discriminate based on disability in hiring, promotion, or termination</li>
                <li><strong>Reasonable Accommodations:</strong> Right to request modifications that enable you to perform job duties</li>
                <li><strong>Privacy:</strong> Medical information must be kept confidential</li>
                <li><strong>Interview Rights:</strong> Employers cannot ask about disabilities before making a job offer</li>
            </ul>
            <p><strong>Filing Complaints:</strong> If you experience discrimination, you can file complaints with relevant labor authorities or disability rights organizations.</p>
