        <div class="resource-content" id="resource-accommodations" style="display: none;">
            <h4>Understanding Workplace Accommodations</h4>
            <p>Workplace accommodations are adjustments or modifications provided by employers to enable employees with disabilities to perform their job duties effectively.</p>
            <h5>Common Types of Accommodations:</h5>
            <ul>
                <li><strong>Physical Modifications:</strong> Wheelchair ramps, adjustable desks, ergonomic equipment</li>
                <li><strong>Schedule Adjustments:</strong> Flexible hours, work-from-home options, modified break schedules</li>
                <li><strong>Technology:</strong> Screen readers, voice recognition software, magnification tools</li>
                <li><strong>Communication:</strong> Sign language interpreters, written instructions, alternative formats</li>
            </ul>
            <p><strong>Your Rights:</strong> Under disability rights laws, employers are required to provide reasonable accommodations unless it causes undue hardship.</p>
